		public init(networkName: String, runMode: Utils.RunMode) throws {
			self.networkName = networkName

			self.debug = false
			self.networkName = networkName
			self.socketGroup = "staff"
			self.socketPath = nil
			self.vmfd = nil
			self.pidFile = nil

			if CakedLib.NetworksHandler.isPhysicalInterface(name: networkName) {
				if CakedLib.NetworksHandler.hasVMNetEntitlement {
					throw ServiceError(String(localized: "Network \(networkName) is handled natively, please use a different network"))
				}

				self.mode = .bridged
				self.macAddress = VZMACAddress.randomLocallyAdministered().string
				self.gateway = nil
				self.dhcpEnd = nil
				self.dhcpLease = nil
				self.subnetMask = "255.255.255.0"
				self.interfaceID = UUID().uuidString
				self.nat66Prefix = nil
			} else {
				let home: Home = try Home(runMode: runMode)
				let networkConfig = try home.sharedNetworks()

				guard let network = networkConfig.sharedNetworks[networkName] else {
					throw ServiceError(String(localized: "Network \(networkName) doesn't exists"))
				}
				guard network.mode != .nat else {
					throw ServiceError(String(localized: "Network \(networkName) is default nated, please use a different network"))
				}

				self.mode = network.mode
				self.gateway = network.dhcpStart
				self.dhcpEnd = network.dhcpEnd
				self.dhcpLease = network.dhcpLease
				self.subnetMask = network.netmask
				self.interfaceID = network.interfaceID
				self.nat66Prefix = network.nat66Prefix
			}
		}

		func validate() throws {
			if self.vmfd != nil && self.socketPath != nil {
				throw ValidationError(String(localized: "fd and socket-path are mutually exclusive \(self.vmfd!) \(self.socketPath!)"))
			}

			if let vmfd = self.vmfd {
				if self.pidFile == nil {
					throw ValidationError(String(localized: "pidfile is required when using fd"))
				}

				guard fcntl(Int32(vmfd), F_GETFD) != -1 || errno != EBADF else {
					throw ValidationError(String(localized: "File descriptor is not open"))
				}
			}

			if self.mode == .bridged {
				if self.networkName == String.empty {
					throw ValidationError(String(localized: "interface is required for bridged mode"))
				}

				if self.gateway != nil {
					throw ValidationError(String(localized: "gateway is not allowed for bridged mode"))
				}

				if self.dhcpEnd != nil {
					throw ValidationError(String(localized: "dhcp-end is not allowed for bridged mode"))
				}
			} else if let gateway = self.gateway {
				guard let gatewayAddr = IP.V4(gateway) else {
					throw ValidationError(String(localized: "gateway is not a valid IP address"))
				}

				guard let dhcpEnd = self.dhcpEnd else {
					throw ValidationError(String(localized: "dhcp-end is required for host/shared mode when gateway is specified"))
				}

				guard let dhcpEndAddr = IP.V4(dhcpEnd) else {
					throw ValidationError(String(localized: "dhcp-end is not a valid IP address"))
				}

				guard self.subnetMask.isValidNetmask() else {
					throw ValidationError(String(localized: "valid netmask is required for host/shared mode when gateway is specified"))
				}

				let cidr = self.subnetMask.netmaskToCidr()
				let network = IP.Block<IP.V4>(base: gatewayAddr, bits: UInt8(cidr)).network

				if network.contains(dhcpEndAddr) == false {
					throw ValidationError(String(localized: "dhcp-end is not in the same network as gateway"))
				}
			}
		}

		func vmnetEndpoint(runMode: Utils.RunMode) throws -> (socket: URL, pidFile: URL) {
			if let socketPath = self.socketPath, let pidFile = self.pidFile {
				return (URL(fileURLWithPath: socketPath), URL(fileURLWithPath: pidFile))
			} else {
				return try CakedLib.NetworksHandler.vmnetEndpoint(networkName: self.networkName, runMode: runMode)
			}
		}

		func createVZVMNet(runMode: Utils.RunMode) throws -> (URL, VZVMNet) {
			let network: VZSharedNetwork

			if CakedLib.NetworksHandler.isPhysicalInterface(name: self.networkName) {
				network = VZSharedNetwork(mode: self.mode, netmask: String.empty, dhcpStart: String.empty, dhcpEnd: String.empty, dhcpLease: nil, interfaceID: self.interfaceID, nat66Prefix: nil)
			} else {
				network = VZSharedNetwork(mode: self.mode, netmask: self.subnetMask, dhcpStart: self.gateway!, dhcpEnd: self.dhcpEnd!, dhcpLease: self.dhcpLease, interfaceID: self.interfaceID, nat66Prefix: self.nat66Prefix)
			}

			guard let grp = getgrnam(self.socketGroup) else {
				throw ServiceError(String(localized: "Failed to get group \(self.socketGroup)"))
			}

			guard let vmfd = self.vmfd else {
				let socketURL = try vmnetEndpoint(runMode: runMode)

				if let dhcpLease = self.dhcpLease, Bundle.isApplicationSandboxed == false {
					_ = try CakedLib.NetworksHandler.setDHCPLease(leaseTime: dhcpLease, runMode: runMode)
				}

				if CakedLib.NetworksHandler.vmnetNative {
					let vzvmnet = VZVMNetNative(
						on: Utilities.group.next(),
						socketGroup: grp.pointee.gr_gid,
						networkName: self.networkName,
						networkConfig: network,
						socketPath: socketURL.socket,
						pidFile: socketURL.pidFile,
						runMode: runMode
					)

					return (socketURL.pidFile, vzvmnet)
				} else if try socketURL.socket.exists() == false {
					let vzvmnet = VZVMNetSocket(
						on: Utilities.group.next(),
						socketGroup: grp.pointee.gr_gid,
						networkName: self.networkName,
						networkConfig: network,
						socketPath: socketURL.socket,
						pidFile: socketURL.pidFile,
						runMode: runMode
					)

					return (socketURL.pidFile, vzvmnet)
				} else {
					throw ServiceError(String(localized: "Socket file already exists at \(socketURL.socket.path)"))
				}
			}

			guard let pidFile = self.pidFile else {
				throw ServiceError(String(localized: "pidfile is required when using vmfd"))
			}

			let pidUrl = URL(fileURLWithPath: pidFile)

			try? pidUrl.delete()

			let vzvmnet = VZVMNetFileHandle(
				on: Utilities.group.next(),
				socketGroup: grp.pointee.gr_gid,
				inputOutput: CInt(vmfd),
				networkName: self.networkName,
				networkConfig: network,
				socketPath: pidUrl.deletingPathExtension().appendingPathExtension("sock"),
				pidFile: pidUrl,
				runMode: runMode)

			return (pidUrl, vzvmnet)
		}
	}

