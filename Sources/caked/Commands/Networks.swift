	struct Create: AsyncParsableCommand {
		static let configuration = NetworkCreateOptions.configuration

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@OptionGroup(title: String(localized: "Create networks options"))
		var options: NetworkCreateOptions

		var createdNetwork: VZSharedNetwork? = nil

		mutating func validate() throws {
			Logger.setLevel(self.common.logLevel)

			let home: Home = try Home(runMode: self.common.runMode)
			let networkConfig = try home.sharedNetworks()

			if networkConfig.sharedNetworks[self.options.name] != nil {
				throw ValidationError(String(localized: "Network \(self.options.name) already exist"))
			}

			if CakedLib.NetworksHandler.isPhysicalInterface(name: self.options.name) {
				throw ValidationError(String(localized: "Network \(self.options.name) is a physical interface"))
			}

			let network = VZSharedNetwork(
				mode: self.options.mode == .shared ? .shared : .host,
				netmask: self.options.subnetMask,
				dhcpStart: self.options.gateway,
				dhcpEnd: self.options.dhcpEnd,
				dhcpLease: self.options.dhcpLease,
				interfaceID: self.options.interfaceID,
				nat66Prefix: self.options.nat66Prefix
			)

			try network.validate(runMode: common.runMode)
			self.createdNetwork = network
		}

		func run() async throws {
			Logger.appendNewLine(self.common.format.render(CakedLib.NetworksHandler.create(networkName: self.options.name, network: self.createdNetwork!, runMode: self.common.runMode)))
		}
	}

	struct Configure: ParsableCommand {
		static let configuration = NetworkConfigureOptions.configuration

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@OptionGroup(title: String(localized: "Configure networks options"))
		var options: NetworkConfigureOptions

		var changedNetwork: VZSharedNetwork? = nil

		mutating func validate() throws {
			Logger.setLevel(self.common.logLevel)

			let home: Home = try Home(runMode: self.common.runMode)
			let networkConfig = try home.sharedNetworks()

			if CakedLib.NetworksHandler.isPhysicalInterface(name: self.options.name) {
				throw ValidationError(String(localized: "Unable to configure physical network \(self.options.name)"))
			}

			if self.options.name == "nat" || self.options.name == CakedLib.IMDSNetworkInterface.imdsNetworkName {
				throw ValidationError(String(localized: "Network \(self.options.name) is a reserved default network and cannot be configured"))
			}

			guard let existing = networkConfig.sharedNetworks[self.options.name] else {
				throw ValidationError(String(localized: "Network \(self.options.name) does not exist"))
			}

			let changed = VZSharedNetwork(
				mode: existing.mode,
				netmask: self.options.subnetMask ?? existing.netmask,
				dhcpStart: self.options.gateway ?? existing.dhcpStart,
				dhcpEnd: self.options.dhcpEnd ?? existing.dhcpEnd,
				dhcpLease: self.options.dhcpLease ?? existing.dhcpLease,
				interfaceID: self.options.interfaceID ?? existing.interfaceID,
				nat66Prefix: self.options.nat66Prefix ?? existing.nat66Prefix
			)

			if existing != changed {
				try changed.validate(runMode: common.runMode)
			}

			self.changedNetwork = changed
		}

		func run() throws {
			Logger.appendNewLine(self.common.format.render(CakedLib.NetworksHandler.configure(networkName: self.options.name, network: self.changedNetwork!, runMode: self.common.runMode)))
		}
	}

	struct Delete: ParsableCommand {
		static let configuration = NetworkDeleteOptions.configuration

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@Argument(help: ArgumentHelp(String(localized: "network name"), discussion: String(localized: "network to delete, e.g. \"shared\"")))
		var name: String

		mutating func validate() throws {
			Logger.setLevel(self.common.logLevel)

			let home: Home = try Home(runMode: self.common.runMode)
			let networkConfig = try home.sharedNetworks()
			let socketURL = try CakedLib.NetworksHandler.vmnetEndpoint(networkName: self.name, runMode: self.common.runMode)

			if FileManager.default.fileExists(atPath: socketURL.socket.path) {
				throw ValidationError(String(localized: "Unable to delete running network \(self.name)"))
			}

			if CakedLib.NetworksHandler.isPhysicalInterface(name: self.name) {
				throw ValidationError(String(localized: "Unable to delete physical network \(self.name)"))
			}

			if networkConfig.sharedNetworks[self.name] == nil {
				throw ValidationError(String(localized: "Network \(self.name) does not exist"))
			}
		}

		func run() throws {
			Logger.appendNewLine(self.common.format.render(CakedLib.NetworksHandler.delete(networkName: self.name, runMode: self.common.runMode)))
		}
	}

	struct Run: AsyncParsableCommand {
		static let configuration = CommandConfiguration(abstract: String(localized: "Run internal VMNet network device"), shouldDisplay: false)

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@OptionGroup(title: String(localized: "Configure VMNet options"))
		var options: Networks.VMNetOptions

		mutating func validate() throws {
			Logger.setLevel(self.common.logLevel)

			let socketURL = try self.options.vmnetEndpoint(runMode: self.common.runMode)

			if FileManager.default.fileExists(atPath: socketURL.socket.path) {
				throw ValidationError(String(localized: "Network already running"))
			}

			if geteuid() != 0 {
				throw ValidationError(String(localized: "This command must be run as root not as user \(geteuid())"))
			}

			if FileManager.default.fileExists(atPath: socketURL.socket.path) {
				throw ValidationError(String(localized: "Network already running"))
			}

			try self.options.validate()
		}

		func run() async throws {
			Logger.appendNewLine(self.common.format.render(Networks.start(options: self.options, fork: true, runMode: self.common.runMode)))
		}
	}

	/// Installs/removes the `pf` redirect that lets the unprivileged `caked service listen`
	/// daemon serve IMDS on port 80 without binding it directly (see `PFRedirect`). Invoked
	/// as a short-lived, separately-privileged helper via `SudoCaked` from `IMDSCoordinator`
	/// — nothing about the daemon itself runs as root.
	struct ImdsRedirect: ParsableCommand {
		static let configuration = CommandConfiguration(abstract: String(localized: "Manage the IMDS pf redirects"), shouldDisplay: false)

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@Flag(help: .hidden)
		var disable: Bool = false

		/// When set, this invocation manages the address-alias redirect (`externalAddress`
		/// → `internalAddress`, any port preserved) instead of the port-80 redirect
		/// (`externalAddress:externalPort` → `internalAddress:internalPort`).
		@Flag(name: [.customLong("alias")], help: .hidden)
		var alias: Bool = false

		@Option(name: [.customLong("external-address")], help: .hidden)
		var externalAddress: String = IMDSNetworkInterface.imdsGateway

		@Option(name: [.customLong("external-port")], help: .hidden)
		var externalPort: Int = 80

		@Option(name: [.customLong("internal-address")], help: .hidden)
		var internalAddress: String = IMDSNetworkInterface.imdsGateway

		@Option(name: [.customLong("internal-port")], help: .hidden)
		var internalPort: Int = 0

		func validate() throws {
			Logger.setLevel(self.common.logLevel)

			if geteuid() != 0 {
				throw ValidationError(String(localized: "This command must be run as root not as user \(geteuid())"))
			}

			if self.disable == false && self.alias == false && self.internalPort == 0 {
				throw ValidationError(String(localized: "--internal-port is required"))
			}
		}

		func run() throws {
			if self.alias {
				if self.disable {
					try PFRedirect.disableAddressAlias()
				} else {
					try PFRedirect.enableAddressAlias(externalAddress: self.externalAddress, targetAddress: self.internalAddress)
				}
			} else if self.disable {
				try PFRedirect.disable()
			} else {
				try PFRedirect.enable(externalAddress: self.externalAddress, externalPort: self.externalPort, internalAddress: self.internalAddress, internalPort: self.internalPort)
			}
		}
	}

	struct Stop: ParsableCommand {
		static let configuration = NetworkStopOptions.configuration

		@OptionGroup(title: String(localized: "Global options"))
		var common: CommonOptions

		@Option(name: [.customLong("pidfile")], help: .hidden)
		var pidFile: String? = nil

		@Argument(help: ArgumentHelp(String(localized: "network name"), discussion: String(localized: "network to stop, e.g., \"en0\" or \"shared\"")))
		var networkName: String? = nil

		func validate() throws {
			Logger.setLevel(self.common.logLevel)

			if networkName != nil && pidFile != nil {
				throw ValidationError(String(localized: "You can only specify one of --network or --pidfile"))
			}

			if networkName == nil && pidFile == nil {
				throw ValidationError(String(localized: "You must specify one of --network or --pidfile"))
			}

			if let networkName {
				try Networks.validateNetwork(networkName: networkName, runMode: self.common.runMode)
			} else if let pidFile {
				if !FileManager.default.fileExists(atPath: pidFile) {
					throw ValidationError(String(localized: "PID file \(pidFile) does not exist"))
				}
			}
		}

		func run() throws {
			if let pidFile {
				Logger.appendNewLine(self.common.format.render(CakedLib.NetworksHandler.stop(pidURL: URL(fileURLWithPath: pidFile), runMode: self.common.runMode)))
			} else if let networkName {
				Logger.appendNewLine(self.common.format.render(CakedLib.NetworksHandler.stop(networkName: networkName, runMode: self.common.runMode)))
			} else {
				throw ValidationError(String(localized: "No network name provided"))
			}
		}
	}

